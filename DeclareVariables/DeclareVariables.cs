using System;


namespace DeclareVariables
{
    class DeclareVariables
    {
        static void Main()
        {
            byte VarableByte = 97;
            sbyte VarableSbyte = -115;
            short VariableShort = -10000;
            ushort VariableUshort = 52130;
            int VariableInt = 4825932;
            Console.WriteLine(VarableByte);
            Console.WriteLine(VarableSbyte);
            Console.WriteLine(VariableShort);
            Console.WriteLine(VariableUshort);
            Console.WriteLine(VariableInt);
        }
    }
}
